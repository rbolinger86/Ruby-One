weather="sunny"
case weather
when 'sunny'
  puts "Break out the flip flops and shades!"
when 'rainy'
  puts "Don't forget your galoshes!"
when 'cold'
  puts "Don't forget your booties! It's cold out there!"
when 'cloudy'
  puts "Bring your umbrella; ominous skies!"
when 'foggy'
  puts "Check your foglights!"
else
  puts "Lock the doors for the apocalypse is upon us."
end
